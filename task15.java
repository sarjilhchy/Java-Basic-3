public class task15 {
      public static void main(String[] args) {
    String txt1 = "We are the so-called \"Vikings\" from the north.";
    System.out.println(txt1); //The sequence \"  inserts a double quote in a string

    String txt2 = "It\'s alright.";
    System.out.println(txt2);  //The sequence \'  inserts a single quote in a string

    String txt3 = "The character \\ is called backslash.";
    System.out.println(txt3); //The sequence \\  inserts a single backslash in a string
  }
}
